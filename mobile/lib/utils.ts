export function formatDate(date: string | Date) {
  // format date nicely
  // example: from this ðŸ‘‰ 2025-05-20 to this ðŸ‘‰ May 20, 2025
  const d = typeof date === "string" ? new Date(date) : date;
  return d.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  });
}